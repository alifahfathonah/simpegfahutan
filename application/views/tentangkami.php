                                <p align="justify">
                                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Fakultas kehutanan Universitas Papua, selanjutnya disebut dengan Fahutan UNIPA, adalah satu-satunya Fakultas kehutanan yang berada di tanah Papua dan berkedudukan di ibukota Provinsi Papua Barat, yaitu Manokwari. Fahutan UNIPA beroperasi dari tahun 2002, yang mana sebelumnya adalah jurusan Kehutanan pada Fakultas Pertanian Universitas Cenderawasih. Pada saat ini, Fahutan memiliki satu jurusan, yaitu jurusan Kehutanan dan membawahi 4 (empat) program studi, yaitu program studi strata satu (S1) kehutanan, dan 3 (tiga) program studi diploma tiga (D3), yaitu Budidaya Hutan (BDH), Konservasi Sumber Daya Hutan (KSDH), dan Manejemen Hutan Alam Produksi (MHAP). 
                                </p>
                                <p align="justify">
                                &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Fahutan Unipa sampai dengan tahun akademik semester gasal 2019/2020, memiliki mahasiswa sebanyak 2677 mahasiswa, terdiri dari 1616 mamasiswa S1 dan 1061 mahasiswa  D3.  Mahasiswa D3 terdiri dari 353 mahasiswa BDH, 336 D3 KSDH, dan 372 D3 MHAP. Fahutan Unipa memiliki tenaga dosen sebanyak 65 orang Status PNS, dengan rincian : Guru besar 1 orang, Lektor Kepala 17 orang, Lektor 31 orang, Asisten Ahli 14 orang, Tenaga Pengajar 2 orang. Tenaga Dosen Tetap Non PNS 3 orang, dan Dosen Luar Biasa 3 orang.
