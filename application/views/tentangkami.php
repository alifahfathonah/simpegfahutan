                                </p>
                                <b>Misi</b> Fahutan Unipa untuk tahun 2015-2019 :
                                <ol type="1">
                                    <li>Menyelenggarakan program pendidikan profesional (akademik) dan teknis (Vokasi) bidang kehutanan melalui penerapan kurikulum berbasis kompetensi;</li>
                                    <li>Menghasilkan inovasi-inovasi penelitian bidang kehutanan dan dipublikasikan dalam jurnal ilmiah bereputasi;</li>
                                    <li>Mengembangkan program pengabdian pada masyarakat dengan teknologi tepat guna yang inovatif berbasis budaya Papua;</li>
                                    <li>Mengembangkan kualitas sumberdaya manusia Fahutan melalui pendidikan lanjut, magang dan pelatihan, serta meningkatkan mutu sarana dan prasarana dalam menunjang Tri Darma perguruan Tinggi;</li>
                                    <li>Mengembangkan sistim manajemen mutu terpadu guna meningkatkan kinerja Fahutan menuju institusi terkemuka;</li>
                                    <li>Menyelenggarakan kerjasama multipihak guna mendukung pelaksanaan  Tridarma perguruan Tinggi.</li>
